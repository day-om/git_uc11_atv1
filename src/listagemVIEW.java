    private void listarProdutos() {
    try {
        
        String idText = id_produto_venda.getText().trim(); 
        Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/uc11?useSSL=false","root","12345");
        ProdutosDAO produtosdao = new ProdutosDAO(conn); 
        List<ProdutosDTO> listagem;

        if (idText.isEmpty()) {
            
            listagem = produtosdao.listarProdutos(-1); 
        } else {
            try {                
                int id = Integer.parseInt(idText);
                listagem = produtosdao.listarProdutos(id); 
            } catch (NumberFormatException e) {
                
                JOptionPane.showMessageDialog(null, "Erro: O ID informado não é válido. Por favor, insira um número.");
                return;
            }
        }        
        if (listagem == null || listagem.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nenhum produto encontrado.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) listaProdutos.getModel();
        model.setNumRows(0);

        listaProdutos.setRowSorter(new TableRowSorter<>(model));

        for (ProdutosDTO p : listagem) {
            Object[] obj = new Object[]{
                p.getId(),
                p.getNome(),
                p.getValor(),
                p.getStatus(),
            };
            model.addRow(obj);
        }

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Erro ao listar produtos: " + e.getMessage());
    }
}



